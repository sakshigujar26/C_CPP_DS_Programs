//program for bitwise operator
 class bitwiseOp{
    public static void main(String[] args) {
        int a = 5;   
        int b = 3;  

        // Bitwise AND
        int resultAnd = a & b; 
        System.out.println("Bitwise AND: " + resultAnd);

        // Bitwise OR
        int resultOr = a | b; 
        System.out.println("Bitwise OR: " + resultOr);

        // Bitwise XOR
        int resultXor = a ^ b;
        System.out.println("Bitwise XOR: " + resultXor);
    }
}

